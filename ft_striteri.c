void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	i = 0;
	while (s[i])
	{
		f(i, &s[i]); //&s[i] passes the pointer/reference to the char instead of the value
		i++;
	}
}
